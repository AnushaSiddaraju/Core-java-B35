package Operators;

public class IncrementandDecrement {

	public static void main(String[] args) {
		
		
	//	increment
		int a=11, b=9;
		a++;//a=11
		++b;//b=9
		int c=--a;
		System.out.println(a);
		System.out.println(b);
		System.out.println(c);
		
		
   // decrement
		int x=15, z=20;
		--x;//x=15
		z--;//z=20
		int y=++x;
		System.out.println(x);
		System.out.println(z);
	    System.out.println(y);
	}

}
